"""Plume compile — regenerate GameData/zzzz_EngineEditor/ from the plume manifest.

Phase-2, Case A only (docs/PlumeEditorDesign.md §5e). This is the ONLY module that writes
plumes to GameData, and it runs solely from an explicit POST /api/plume/compile (the user
clicking "Compile / Apply"). It:

  1. Writes EngineEditor_templates.cfg — every custom template as an EFFECTTEMPLATE
     (tool-only keys stripped: `_eeDisabled`; effects flagged `_eeDisabled=true` omitted).
  2. Writes one ee_plume_<part>.cfg per assigned engine — a Case-A `@PART[...]:FINAL`
     ModuleB9PartSwitch (moduleID=eePlumeSwitch) with a "Stock" default subtype + one
     subtype per variant that overrides the part's ModuleWaterfallFX TEMPLATE.
  3. VALIDATES first (docs/ModuleManager.md §7 lint). On ANY violation nothing is written.

Idempotent: our own artifacts (EngineEditor_templates.cfg + ee_plume_*.cfg) are rewritten
each run and stale ones removed; other files in the folder (e.g. /api/save_patch output) are
left untouched.
"""
import os

HERE = os.path.dirname(os.path.abspath(__file__))
KSP = os.path.dirname(os.path.dirname(HERE))
OUT_DIR = os.path.join(KSP, 'GameData', 'zzzz_EngineEditor')

TEMPLATES_FILE = 'EngineEditor_templates.cfg'
ENGINE_PREFIX = 'ee_plume_'
SWITCH_MODULE_ID = 'eePlumeSwitch'
VARIANT_PREFIX = 'ee_variant_'
ENGINE_SWITCH_MODULE_ID = 'eeEngineSwitch'
# §10.1 — the ModuleWaterfallFX we mint for engines that ship without one.
MINT_WF_MODULE_ID = 'eeWaterfall'


class CompileError(Exception):
    def __init__(self, errors):
        self.errors = errors
        super().__init__('; '.join(errors))


# ---------------- cfg serialization (port of plumelib.js serialize) ----------------

def _is_disabled(node):
    for k, v in node.get('k', []):
        if k == '_eeDisabled' and v == 'true':
            return True
    return False


def _serialize(node, depth, out):
    """Serialize a {h,k,c} node to cfg text lines. Skips _eeDisabled keys and skips whole
    EFFECT children flagged `_eeDisabled=true`."""
    pad = '  ' * depth
    out.append(pad + node['h'])
    out.append(pad + '{')
    for k, v in node.get('k', []):
        if k == '_eeDisabled':
            continue
        out.append(pad + '  ' + str(k) + ' = ' + str(v))
    for c in node.get('c', []):
        if c.get('h') == 'EFFECT' and _is_disabled(c):
            continue
        _serialize(c, depth + 1, out)
    out.append(pad + '}')


def _set_key(node, key, value):
    for kv in node['k']:
        if kv[0] == key:
            kv[1] = value
            return
    node['k'].append([key, value])


def _deep(node):
    import json
    return json.loads(json.dumps(node))


def template_cfg(manifest):
    """The EngineEditor_templates.cfg body: each custom template as an EFFECTTEMPLATE whose
    templateName is forced to the custom name."""
    out = ['// Generated by EngineEditor — do not edit by hand.',
           '// Custom Waterfall templates (source of truth: EngineEditor/data/plume_project.json).', '']
    for name in sorted(manifest.get('templates', {})):
        tree = _deep(manifest['templates'][name]['tree'])
        tree['h'] = 'EFFECTTEMPLATE'
        _set_key(tree, 'templateName', name)
        _serialize(tree, 0, out)
        out.append('')
    return '\n'.join(out) + '\n'


def engine_cfg(part, eng):
    """A single Case-A @PART[...]:FINAL B9PS plume switcher."""
    L = []
    L.append('// Generated by EngineEditor — do not edit by hand.')
    L.append('@PART[%s]:FINAL' % part)
    L.append('{')
    L.append('  MODULE')
    L.append('  {')
    L.append('    name = ModuleB9PartSwitch')
    L.append('    moduleID = %s' % SWITCH_MODULE_ID)
    L.append('    switcherDescription = EngineEditor Plume')
    wf_id = eng.get('wfModuleID', '')
    for v in eng.get('variants', []):
        name = v.get('name', '')
        L.append('    SUBTYPE')
        L.append('    {')
        L.append('      name = %s' % name)
        if name != 'Stock' and v.get('template'):
            L.append('      MODULE')
            L.append('      {')
            L.append('        IDENTIFIER')
            L.append('        {')
            L.append('          name = ModuleWaterfallFX')
            L.append('          moduleID = %s' % wf_id)
            L.append('        }')
            L.append('        DATA')
            L.append('        {')
            L.append('          !TEMPLATE,*{}')
            L.append('          TEMPLATE')
            L.append('          {')
            L.append('            templateName = %s' % v.get('template'))
            L.append('            overrideParentTransform = %s' % (v.get('overrideParentTransform', '') or ''))
            L.append('            position = %s' % (v.get('position', '0,0,0') or '0,0,0'))
            L.append('            rotation = %s' % (v.get('rotation', '0,0,0') or '0,0,0'))
