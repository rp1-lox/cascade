def compile_all(manifest, mod_template_names, part_ctx, variant_ctx=None, out_dir=OUT_DIR):
    """Validate, then rewrite our artifacts in out_dir. Raises CompileError (writes nothing)
    on any lint violation. Returns a summary dict. `variant_ctx` (default {}) is
    {part: engine_variant_info(part)} for every part in manifest['engineVariants']; callers that
    pass none behave exactly as before."""
    variant_ctx = variant_ctx or {}
    errors = validate(manifest, mod_template_names, part_ctx, variant_ctx)
    if errors:
        raise CompileError(errors)

    files = {}  # basename -> content
    files[TEMPLATES_FILE] = template_cfg(manifest)
    assigned = []
    for part in sorted(manifest.get('engines', {})):
        eng = manifest['engines'][part]
        if eng.get('case') == 'B':
            # skip if no real Case-B edit
            if not (eng.get('editBase') and eng['editBase'].get('template')) \
               and not eng.get('editSubtypes') and not eng.get('addVariants'):
                continue
            files[ENGINE_PREFIX + part + '.cfg'] = engine_cfg_b(part, eng, part_ctx.get(part))
            assigned.append(part)
            continue
        # Case A: skip engines that only have Stock (no real assignment) — nothing to emit
        if not any(v.get('name') != 'Stock' for v in eng.get('variants', [])):
            continue
        files[ENGINE_PREFIX + part + '.cfg'] = engine_cfg(part, eng)
        assigned.append(part)

    # Engine-config variants — one ee_variant_<part>.cfg per part with subtypes.
    variant_parts = []
    for part in sorted(manifest.get('engineVariants', {})):
        ev = manifest['engineVariants'][part]
        if not ev.get('subtypes'):
            continue
        files[VARIANT_PREFIX + part + '.cfg'] = engine_variant_cfg(part, ev, variant_ctx.get(part))
        variant_parts.append(part)

    # If there are no custom templates AND no assignments, don't leave an empty templates file.
    if not manifest.get('templates') and not assigned and not variant_parts:
        files.pop(TEMPLATES_FILE, None)

    os.makedirs(out_dir, exist_ok=True)
    # remove stale artifacts we own that aren't in the new set
    for fn in os.listdir(out_dir):
        if fn == TEMPLATES_FILE or fn.startswith(ENGINE_PREFIX) or fn.startswith(VARIANT_PREFIX):
            if fn not in files:
                try:
                    os.remove(os.path.join(out_dir, fn))
                except OSError:
                    pass

    written = []
    for fn, content in files.items():
        with open(os.path.join(out_dir, fn), 'w', encoding='utf-8', newline='\n') as f:
            f.write(content)
        written.append('GameData/zzzz_EngineEditor/' + fn)

    return {'ok': True, 'written': sorted(written), 'assignedEngines': assigned,
            'variantEngines': variant_parts,
            'customTemplates': sorted(manifest.get('templates', {}))}
